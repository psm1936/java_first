package kr.smhrd.model;

public class RentalshopVO {
	private String rentalshopid;
	private String rentalshopname;
	private String rentalshopaddress;
	private String latitude;
	private String longitude;
	
	public String getRentalshopid() {
		return rentalshopid;
	}
	public void setRentalshopid(String rentalshopid) {
		this.rentalshopid = rentalshopid;
	}
	public String getRentalshopname() {
		return rentalshopname;
	}
	public void setRentalshopname(String rentalshopname) {
		this.rentalshopname = rentalshopname;
	}
	public String getRentalshopaddress() {
		return rentalshopaddress;
	}
	public void setRentalshopaddress(String rentalshopaddress) {
		this.rentalshopaddress = rentalshopaddress;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	
	
}
