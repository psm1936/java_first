package kr.smhrd.model;

public class HelmetVO {
	private String helmetid;
	private String helmetsize;
	private int loss;
	private String helmetregister;
	private int disinfection;
	private String rentalshopid;
	public String getHelmetid() {
		return helmetid;
	}
	public void setHelmetid(String helmetid) {
		this.helmetid = helmetid;
	}
	public String getHelmetsize() {
		return helmetsize;
	}
	public void setHelmetsize(String helmetsize) {
		this.helmetsize = helmetsize;
	}
	public int getLoss() {
		return loss;
	}
	public void setLoss(int loss) {
		this.loss = loss;
	}
	public String getHelmetregister() {
		return helmetregister;
	}
	public void setHelmetregister(String helmetregister) {
		this.helmetregister = helmetregister;
	}
	public int getDisinfection() {
		return disinfection;
	}
	public void setDisinfection(int disinfection) {
		this.disinfection = disinfection;
	}
	public String getRentalshopid() {
		return rentalshopid;
	}
	public void setRentalshopid(String rentalshopid) {
		this.rentalshopid = rentalshopid;
	}

}


